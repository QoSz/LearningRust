fn main() {
    let fname: &str = "Zo";
    let lname: &str = "Low";

    if fname == "John" {
        println!("The first name is John");
    } else if lname == "Doe" {
        println!("The last name is Doe");
    } else {
        println!("The first name is not John and the last name is not Doe");
    }

}